package ch.makery.address.model;

public class Report {

	private double totalSubAmount;
	private double totalProductGstCharge;
	private double totalGstAmount;
	
	
	public double getTotalSubAmount() {
		return totalSubAmount;
	}
	public void setTotalSubAmount(double totalSubAmount) {
		this.totalSubAmount = totalSubAmount;
	}
	public double getTotalProductGstCharge() {
		return totalProductGstCharge;
	}
	public void setTotalProductGstCharge(double totalProductGstCharge) {
		this.totalProductGstCharge = totalProductGstCharge;
	}
	public double getTotalGstAmount() {
		return totalGstAmount;
	}
	public void setTotalGstAmount(double totalGstAmount) {
		this.totalGstAmount = totalGstAmount;
	}
	
	
}
